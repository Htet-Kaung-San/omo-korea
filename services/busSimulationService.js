async function getLiveBusLocations() {
  const apiKey = process.env.BUSAN_BIS_API_KEY;
  const lineId = "5291107000"; // Line ID for Geumjeong-gu 7
  
  if (!apiKey) return [];

  try {
    const url = `http://apis.data.go.kr/6260000/BusanBIMS/busInfoByRouteId?lineid=${lineId}&serviceKey=${apiKey}`;
    const response = await fetch(url);
    const text = await response.text();
    
    if (text.includes("<lat>") && text.includes("<lin>")) {
      const buses = [];
      const items = text.split("<item>");
      
      for (let i = 1; i < items.length; i++) {
        const latMatch = items[i].match(/<lat>(.*?)<\/lat>/);
        const lngMatch = items[i].match(/<lin>(.*?)<\/lin>/);
        const plateMatch = items[i].match(/<carno>(.*?)<\/carno>/);
        
        if (latMatch && lngMatch && plateMatch) {
          buses.push({
            id: `bus-real-${i}`,
            licensePlate: plateMatch[1],
            lat: parseFloat(latMatch[1]),
            lng: parseFloat(lngMatch[1]),
            route: "Geumjeong-gu 7",
          });
        }
      }
      return buses;
    }
  } catch (e) {
    console.error("Failed to fetch real bus data.", e);
  }

  return [];
}

